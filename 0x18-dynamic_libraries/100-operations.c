/**
  *add - add two integers
  *@a: integer
  *@b: integer
  *Return: sum
  */
int add(int a, int b)
{
    return (a+b);
}

/**
  *sub - substruct two integers
  *@a: integer
  *@b: integer
  *Return: difference
  */
int sub(int a, int b)
{
    return (a-b);
}

/**
  *mul - multiply add two integers
  *@a: integer
  *@b: integer
  *Return: product
  */
int mul(int a, int b)
{
    return (a*b);
}

/**
  *div - divide two integers
  *@a: integer
  *@b: integer
  *Return: quotient
  */
int div(int a, int b)
{
    return (a/b);
}

/**
  *mod - modulo of numbers
  *@a: integer
  *@b: integer
  *Return: result of the operation
  */
int mod(int a, int b)
{
    return (a%b);
}
